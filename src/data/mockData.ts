const generateTempCurve = (
  targetTemp: number,
  points: number = 12
): TempPoint[] => {
  const curve: TempPoint[] = [];
  for (let i = 0; i < points; i++) {
    const time = i * 30;
    let temp: number;
    if (i < 3) {
      temp = Math.round(20 + (targetTemp - 20) * (i / 3) + (Math.random() - 0.5) * 10);
    } else if (i < points - 2) {
      temp = Math.round(targetTemp + (Math.random() - 0.5) * 8);
    } else {
      temp = Math.round(targetTemp - 50 * (i - (points - 2)) + (Math.random() - 0.5) * 5);
    }
    curve.push({ time, temp: Math.max(20, temp) });
  }
  return curve;
};

